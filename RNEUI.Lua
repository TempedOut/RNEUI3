								return

							end

						end

						alreadyneeds = false


					end

				end



			end

		end


		local checkHunger = maxHunger.Value * 0.9
		local checkThirst = maxThirst.Value * 0.9
		local checkSleep = maxSleep.Value * 0.9

		if alreadyneeds == false then

			if checkHunger >= curHunger.Value then

				--Eat

				if game.PlaceId == IDWhaleIsland then
					myHMRP.CFrame = CFrame.new(-65.8969727, 457.260803, -1076.46057)
					wait(0.1)
					pressF(1)



				elseif game.PlaceId == IDDesert then

				elseif game.PlaceId == IDYorkNew then
					myHMRP.CFrame = CFrame.new(202.02124, 111.94091, 124.317574)
					wait(0.1)
					pressF(1)


				end

			end

			if checkThirst >= curThirst.Value then

				--Drink

				if game.PlaceId == IDWhaleIsland then
					myHMRP.CFrame = CFrame.new(-29.4432106, 457.438324, -1089.86926)
					wait(0.1)
					pressF(1)


				elseif game.PlaceId == IDDesert then

				elseif game.PlaceId == IDYorkNew then
					myHMRP.CFrame = CFrame.new(202.02124, 111.94091, 124.317574)
					wait(0.1)
					pressF(1)


				end

			end

			if checkSleep >= curSleep.Value then

				--Sleep

				if game.PlaceId == IDWhaleIsland then
					myHMRP.CFrame = CFrame.new(791.467285, 473.87677, -672.494141)
					wait(0.1)
					pressF(1)


				elseif game.PlaceId == IDDesert then

				elseif game.PlaceId == IDYorkNew then
					myHMRP.CFrame = CFrame.new(192.826385, 111.940926, 158.302139)
					wait(0.1)
					pressF(1)


				end

			end

			alreadyneeds = true

		end        

		if RNEV3.CashViewer.Visible == true then

			RNEV3.CashViewer.Visible = false

		end

		tele = CFrame.new(881.397644, 200005, 90.6582718)

		if alreadyhere == false then
			myHMRP.CFrame = tele
			alreadyhere = true
		end

		if resting == false then

			if UIS.MouseBehavior == Enum.MouseBehavior.Default then
				press("tab", 1, 0.5)
				wait(1)
			end

			press("z", 1, 0.5)
			wait(2)

			resting = true
		end


	end

	if game.PlaceId == IDWhaleIsland then

	elseif game.PlaceId == IDDesert then

	elseif game.PlaceId == IDYorkNew then


	end


	myHMRP.Anchored = false
