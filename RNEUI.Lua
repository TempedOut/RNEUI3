local function menuControl()

	if MenuBool == true then

		MenuBool = false

		MainContainer:TweenSize(UDim2.new(0,454,0,38),Enum.EasingDirection.Out,Enum.EasingStyle.Linear,.5)

		for i,v in pairs(MainContainer:GetChildren()) do

			if v.Name ~= "MenuToggle" and v.Name ~= "MenuTitle" then
				v.Visible = false
			end

		end

		MenuToggle.Text = "[OPEN]"
	elseif MenuBool == false then

		MenuBool = true

		MainContainer:TweenSize(UDim2.new(0,454,0,525),Enum.EasingDirection.Out,Enum.EasingStyle.Linear,.5)

		if ToggleFrameBool == true then

			TogglesFrame.Visible = true

		elseif PlayerFrameBool == true then

			PlayersFrame.Visible = true

		elseif EspFrameBool == true then

			EspFrame.Visible = true

		elseif MobFrameBool == true then

			MobFrame.Visible = true

		elseif SettingsFrameBool == true then

			SettingsFrame.Visible = true

		end

		MenuToggleContainer.Visible = true

		MenuToggle.Text = "[CLOSE]"

	end

end

